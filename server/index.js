app.put('/api/users/:id', authenticate, async (req, res) => {
  try {
    const { id } = req.params;
    const {
      name,
      email,
      phone,
      birth_date,
      address,
      address_number,
      address_complement,
      neighborhood,
      city,
      state,
      roles,
      percentage,
      category_id,
      currentPassword,
      newPassword,
      crm,
      specialty
    } = req.body;

    // Check if user exists
    const userResult = await pool.query('SELECT * FROM users WHERE id = $1', [id]);
    if (userResult.rows.length === 0) {
      return res.status(404).json({ message: 'Usu√°rio n√£o encontrado' });
    }

    const user = userResult.rows[0];

    // If changing password, verify current password
    if (newPassword) {
      if (!currentPassword) {
        return res.status(400).json({ message: 'Senha atual √© obrigat√≥ria para alterar a senha' });
      }

      const isValidPassword = await bcrypt.compare(currentPassword, user.password);
      if (!isValidPassword) {
        return res.status(400).json({ message: 'Senha atual incorreta' });
      }
    }

    // Prepare update data
    let updateQuery = `
      UPDATE users SET 
        name = $1, email = $2, phone = $3, birth_date = $4, address = $5, 
        address_number = $6, address_complement = $7, neighborhood = $8, 
        city = $9, state = $10, roles = $11, percentage = $12, category_id = $13, 
        crm = $14, updated_at = NOW()
    `;
    
    let updateValues = [
      name, email, phone, birth_date, address, address_number,
      address_complement, neighborhood, city, state, roles, percentage, category_id, crm
    ];

    // Add password to update if provided
    if (newPassword) {
      const hashedPassword = await bcrypt.hash(newPassword, 10);
      updateQuery += ', password = $15';
      updateValues.push(hashedPassword);
    }

    updateQuery += ' WHERE id = $' + (updateValues.length + 1) + ' RETURNING id, name, cpf, roles';
    updateValues.push(id);

    const result = await pool.query(updateQuery, updateValues);

    res.json({
      message: 'Usu√°rio atualizado com sucesso',
      user: result.rows[0]
    });
  } catch (error) {
    console.error('Error updating user:', error);
    res.status(500).json({ message: 'Erro interno do servidor' });
  }
});

